DELIMITER $$

CREATE PROCEDURE populate_dummy_data()
BEGIN
  DECLARE i INT DEFAULT 7;
  DECLARE keyword_count INT DEFAULT (SELECT COUNT(*) FROM keywords);
  DECLARE q_id BIGINT DEFAULT 17;
  WHILE i <= 100 DO
    INSERT INTO questions (keyword_id, question_text)
    VALUES (FLOOR(1 + RAND() * keyword_count), CONCAT('မြန်မာစိုက်ပျိုးရေးမေးခွန်းနံပါတ် ', i));

    INSERT INTO answers (question_id, answer_text)
    VALUES (q_id, CONCAT('မြန်မာစိုက်ပျိုးရေးအဖြေ နံပါတ် ', i));

    SET i = i + 1;
    SET q_id = q_id + 1;
  END WHILE;
END $$

DELIMITER ;

-- Call the procedure to insert dummy data
CALL populate_dummy_data();

